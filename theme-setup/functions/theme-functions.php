<?php /* ======================================================================

  THEME CORE FUNCTIONS

===============================================================================

  REMOVE ADMIN BAR

===============================================================================

  Since:        0.0.001
  Last Updated: 0.0.001

  Removes the admin bar from the front end.

============================================================================ */

show_admin_bar( false );


/* ============================================================================

  CHANGE ADMIN FOOTER

===============================================================================

  Since:        0.0.001
  Last Updated: 0.0.201

  Changes the default admin footer to display a link to We Are Empire.

============================================================================ */

function ts_emp_admin_footer() {

  echo 'Made by <a href="http://www.weareempire.co.uk" target="_blank">We Are Empire</a></p>';

}

add_filter( 'admin_footer_text', 'ts_emp_admin_footer' );


/* ============================================================================

  REGISTER NAV MENUS

===============================================================================

  Since:        0.0.215
  Last Updated: 0.0.215

  - Registers a primary and secondary menu.

============================================================================ */

register_nav_menus( array(

  'primary' => __( 'Primary', 'empire_base' ), 'secondary'  => __( 'Secondary', 'empire_base' ), 'mobile'  => __( 'Mobile', 'empire_base' )

) );


/* ============================================================================

  REMOVE DEFAULT META BOXES

===============================================================================

  Since:        0.0.216
  Last Updated: 0.0.216

  - Removes unused metaboxes

============================================================================ */

function ts_emp_remove_metaboxes() {


/* ============================================================================

  CUSTOM POSTS

============================================================================ */

  remove_meta_box( 'postcustom', 'page', 'normal' );


/* ============================================================================

  EXCERPT

============================================================================ */

  remove_meta_box( 'postexcerpt', 'page', 'normal' );


/* ============================================================================

  COMMENTS

============================================================================ */

  remove_meta_box( 'commentstatusdiv', 'page', 'normal' );


/* ============================================================================

  PING / TRACK BACK

============================================================================ */

  remove_meta_box( 'trackbacksdiv', 'page', 'normal' );


/* ============================================================================

  SLUG

============================================================================ */

  remove_meta_box( 'slugdiv', 'page', 'normal' );


/* ============================================================================

  AUTHOR

============================================================================ */

  remove_meta_box( 'authordiv', 'page', 'normal' );


/* ============================================================================

  POST IMAGE

============================================================================ */

  remove_meta_box( 'postimagediv', 'page', 'normal' );

}

add_action( 'admin_menu', 'ts_emp_remove_metaboxes' );
